#include <stdio.h>
int main()
{
// 左移   << 高位舍弃低位补０
//m 左移ｎ位　== m*2的n次方
    int a = 2;
    a<<=4;
    printf ("a = %d\n",a);
//00 0000 0000 0000 0000 0000 0000 00 10 00
//右移运算　>> 低位舍弃　高位补0/1;
//m 右移ｎ位　== m/2的n次方
    a>>=2;
    printf ("a=%d\n",a);
//a = 0000 0000 0000 0000 0000 0000 0000 0101
 //   a>>32&1;



}
